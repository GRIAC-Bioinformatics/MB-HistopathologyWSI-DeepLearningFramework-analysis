# Function to apply grad-cam (heatmaps visualization)
def grad_cam(image, model, eps=1e-8):
    layerName = 'conv_7b_ac'
    gradModel = Model(
          inputs=[model.inputs],
          outputs=[model.get_layer(layerName).output,
            model.output])

    with tf.GradientTape() as tape:
          # cast the image tensor to a float-32 data type, pass the
          # image through the gradient model, and grab the loss
          # associated with the specific class index
          (convOutputs, predictions) = gradModel(np.expand_dims(image, axis=0))
          loss = predictions[:, 0]
    # use automatic differentiation to compute the gradients
    grads = tape.gradient(loss, convOutputs)

    # compute the guided gradients
    castConvOutputs = tf.cast(convOutputs > 0, "float32")
    castGrads = tf.cast(grads > 0, "float32")
    guidedGrads = castConvOutputs * castGrads * grads
    # the convolution and guided gradients have a batch dimension
    # (which we don't need) so let's grab the volume itself and
    # discard the batch
    convOutputs = convOutputs[0]
    guidedGrads = guidedGrads[0]

    # compute the average of the gradient values, and using them
    # as weights, compute the ponderation of the filters with
    # respect to the weights
    weights = tf.reduce_mean(guidedGrads, axis=(0, 1))
    cam = tf.reduce_sum(tf.multiply(weights, convOutputs), axis=-1)

    (w, h) = (image.shape[0], image.shape[1])
    heatmap_org = cv2.resize(cam.numpy(), (w, h))
    # normalize the heatmap such that all values lie in the range
    # [0, 1], scale the resulting values to the range [0, 255],
    # and then convert to an unsigned 8-bit integer
    numer = heatmap_org - np.min(heatmap_org)
    denom = (heatmap_org.max() - heatmap_org.min()) + eps
    heatmap = numer / denom
    heatmap = (heatmap * 255).astype("uint8")

    # Numerical heatmap (without mapping to colors)
    heatmap_num = cv2.resize(heatmap, (image.shape[0], image.shape[1]))
    heatmap_col = cv2.applyColorMap(heatmap_num, colormap=cv2.COLORMAP_JET)

    return heatmap_org, heatmap_col


def split_data(data, enc_labels, labels, independent):

    indices = np.arange(len(labels))

    if not independent:
        # Split train - test
        ind_train, ind_test = train_test_split(indices,
                                                test_size=0.10,
                                                random_state=42,
                                                stratify=labels['Tiff'],
                                                shuffle=True)
        # Split train - validation
        ind_train, ind_val = train_test_split(ind_train,
                                                test_size=0.15/0.9,
                                                random_state=42,
                                                stratify=labels.iloc[ind_train,-1])

    else:
        # Initialize group splitter, based on Tiff key
        split_tr_te = GroupShuffleSplit(n_splits=2, train_size=.9, random_state=42)
        split_tr_val = GroupShuffleSplit(n_splits=2, train_size=.75, random_state=42)

        # Split train - test and train - validation
        ind_train, ind_test = next(split_tr_te.split(data, groups=labels['Tiff']))
        ind_train, ind_val = next(split_tr_val.split(data[ind_train], groups=labels.iloc[ind_train,-1]))

        X_train = data[ind_train]
        y_train = enc_labels[ind_train]
        X_test = data[ind_test]
        y_test = enc_labels[ind_test]
        X_val = data[ind_val]
        y_val = enc_labels[ind_val]

    return ind_train, ind_val, ind_test


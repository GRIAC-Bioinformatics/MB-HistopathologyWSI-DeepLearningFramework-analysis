# Calculate predictions, save plots etc
def summarize_model(model, history, file_add):

    # Get predictions
    preds_train = model.predict(X_train)
    preds_val = model.predict(X_val)
    preds_test = model.predict(X_test)

    # Calculate AUC on test set
    fpr_test, tpr_test, thresholds_keras = roc_curve(y_test, preds_test.ravel())
    auc_test = auc(fpr_test, tpr_test)

    # Get summary per TIFF
    labels_train = labels.iloc[ind_train,-1]
    labels_val = labels.iloc[ind_val,-1]
    labels_test = labels.iloc[ind_test,-1]

    df = pd.DataFrame()
    df['Tiff'] = np.concatenate((labels_train, labels_val, labels_test))
    df['set'] = np.concatenate((np.repeat('train', len(labels_train)), np.repeat('val', len(labels_val)), np.repeat('test', len(labels_test))))
    df['label'] = np.concatenate((y_train, y_val, y_test))
    df['pred'] = np.concatenate(((preds_train[:,0] >= 0.5).astype(int), (preds_val[:,0] >= 0.5).astype(int), (preds_test[:,0] >= 0.5).astype(int)))

    # Indicators for true / false positive / negative
    df['tn'] = ((df.label == 0) & (df.pred == 0)).astype(int)
    df['fn'] = ((df.label == 1) & (df.pred == 0)).astype(int)
    df['tp'] = ((df.label == 1) & (df.pred == 1)).astype(int)
    df['fp'] = ((df.label == 0) & (df.pred == 1)).astype(int)

    # Split into train and validation and test
    df_test_val = df[df['set'] != 'train']
    df_train = df[df['set'] == 'train']

    # Group by tiff to get total accuracy
    acc_test_val = df_test_val.groupby(['set','Tiff']).sum().reset_index()
    acc_test_val['perc_outside'] = acc_test_val.label / (acc_test_val.pred + acc_test_val.tn + acc_test_val.fn)
    acc_test_val['accuracy'] = (acc_test_val.tp + acc_test_val.tn) / (acc_test_val.pred + acc_test_val.tn + acc_test_val.fn)

    acc_train = df_train.groupby(['set','Tiff']).sum().reset_index()
    acc_train['perc_outside'] = acc_train.label / (acc_train.pred + acc_train.tn + acc_train.fn)
    acc_train['accuracy'] = (acc_train.tp + acc_train.tn) / (acc_train.pred + acc_train.tn + acc_train.fn)

    # Left join train acc per TIFF
    acc_per_tiff = pd.merge(acc_test_val, acc_train, how='left', on='Tiff')

    # Save as csv
    acc_per_tiff.to_csv(output_path + '/acc_per_tiff.csv')

    # Summarize history for AUC
    plt.figure()
    plt.plot(history.history['auc'])
    plt.plot(history.history['val_auc'])
    plt.title('model AUC')
    plt.ylabel('AUC')
    plt.xlabel('Epoch')
    plt.legend(['Train', 'Validation'], loc='upper left')
    plt.savefig(output_path + '/auc_dev.png')

    # Summarize history for AUC
    plt.figure()
    plt.plot(history.history['loss'])
    plt.plot(history.history['val_loss'])
    plt.title('model loss')
    plt.ylabel('Loss')
    plt.xlabel('Epoch')
    plt.legend(['Train', 'Validation'], loc='upper left')
    plt.savefig(output_path + '/loss_dev.png')

    # Plot confusion matrix - test set
    cm = confusion_matrix(y_test, (preds_test[:,0] >= 0.5).astype(int))
    disp = ConfusionMatrixDisplay(confusion_matrix=cm,
                                  display_labels=['Inside','Outside'])
    disp = disp.plot(include_values=True, cmap=plt.cm.Blues,
                     ax=None, xticks_rotation='horizontal',values_format = '.5g')
    plt.savefig(output_path + '/confusion_test_' + file_add + '.png')

    return auc_test

